// Faça um programa que receba o nome, o peso e a altura de uma pessoa. Calcule e imprima o nome e o IMC

// Entrada
let nome = "Fábio";
let peso = 100; // em quilogramas
let altura = 1.96; // em metros

// Processamento
let imc = peso / (altura*altura);

// Saída
console.log("O IMC do " + nome + " é: " + imc )
